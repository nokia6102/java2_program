public class SampleD3L5 {
	public static void main(String args[]) {

		String myStr = new String("運用String類別與Math類別");
		System.out.println(myStr);

		System.out.println("利用sqrt方法求出平方根");
		System.out.println("16的平方根是" + Math.sqrt(16));

		System.out.println("利用pow方法求出某一個數字的乘方");
		System.out.println("8的3次方是" + Math.pow(8, 3));

		System.out.println("利用round方法進行四捨五入");
		System.out.println("21.859四捨五入後是" + Math.round(21.859));
	}
}



