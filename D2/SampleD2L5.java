public class SampleD2L5 {
	public static void main(String args[]) {

		// 變數的宣告
		int intA;
		int intB;
		char chrA;

		// 將值代入變數
		intA = 10;
		intB = 2;
		chrA = 'a';

		// 在控制台上顯示訊息
		System.out.println("變數intA中存放著" + intA + "這個值");
		System.out.println("變數intB中存放著" + intB + "這個值");
		System.out.println("變數chrA中存放著" + chrA + "這個值");


		System.out.println("intA + intB = " + (intA + intB));
		System.out.println("intA - intB = " + (intA - intB));

	}
}